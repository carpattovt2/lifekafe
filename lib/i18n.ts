export type Lang = 'en' | 'ua'

export function formatDate(ds: string, lang: Lang): string {
  if (!ds) return ''
  const [y, m, d] = ds.split('-').map(Number)
  return new Date(y, m - 1, d).toLocaleDateString(
    lang === 'ua' ? 'uk-UA' : 'en-US',
    { year: 'numeric', month: 'long', day: 'numeric' }
  )
}

